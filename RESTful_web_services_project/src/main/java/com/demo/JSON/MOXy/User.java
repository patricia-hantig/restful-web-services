package com.demo.JSON.MOXy;

import javax.xml.bind.annotation.XmlRootElement;

// a simple Java bean representing a user entity
// @XmlRootElement = indicates that instances of this class can be serialized and deserialized to XML or JSON
@XmlRootElement
public class User {

    private int id;
    private String name;

    public User() {
    }

    public User(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "User{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
